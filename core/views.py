class CategoryView(View):
    def get(self, *args, **kwargs):
        category = get_object_or_404(Category, slug=self.kwargs['slug'], is_active=True)
        items = Item.objects.filter(category=category, is_active=True)
        
        # Apply search filtering within category
        search_query = self.request.GET.get('q')
        if search_query:
            items = items.filter(
                Q(title__icontains=search_query) |
                Q(description_short__icontains=search_query) |
                Q(description_long__icontains=search_query)
            )
        
        context = {
            'object_list': items,
            'category_title': category,
            'category_description': category.description,
            'category_image': category.image,
            'search_form': SearchForm(self.request.GET or None)
        }
        return render(self.request, "category.html", context)


